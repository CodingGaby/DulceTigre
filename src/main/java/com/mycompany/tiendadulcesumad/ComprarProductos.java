    public void calcularPrecio(){
        double price = inventario.get(jComboBox1.getSelectedIndex()).obtenerPrecioConIVA();

        jLabel10.setText(aMoneda(price));
    }

    public String aMoneda(double precio){
        return Math.round(precio*100.0)/100.0 + " MXN";
    }

    public void actualizarTabla() {
        double impTot = 0;

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

        for (Venta v : listaVentas){
            Object x[] = new Object[4];
            x[0]=v.getDesc();
            x[1]= aMoneda(v.getPrecio());
            x[2]=v.getCant();
            x[3]= aMoneda(v.getImporte());
            impTot += v.getImporte();
            modelo.addRow(x);
        }

        jTable1.setModel(modelo);
        jLabel9.setText(aMoneda(impTot));
    }

