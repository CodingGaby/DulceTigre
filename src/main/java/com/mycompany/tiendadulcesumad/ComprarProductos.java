    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cantidad = Integer.parseInt(jSpinner1.getValue().toString());
        Venta venta = new Venta();
        venta.setId(jComboBox1.getSelectedIndex());
        venta.setDesc(jComboBox1.getSelectedItem().toString());
        venta.setPrecio(inventario.get(jComboBox1.getSelectedIndex()).getPrecio());
        venta.setCant(cantidad);
        venta.setImporte(venta.getPrecio() * cantidad);
        listaVentas.add(venta);
        actualizarTabla();
    }//GEN-LAST:event_jButton1ActionPerformed

